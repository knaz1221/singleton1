package practice2.college.practice2.builder;

public class Size {
    private int height;
    private int width;

    public Size(int height, int width) {
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(short height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(short width) {
        this.width = width;
    }
}